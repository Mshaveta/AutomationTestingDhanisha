package variables;

public class PrimitiveDataTypes {
    public static void main(String[] args) {
        byte b = 125;
        System.out.println(b);

        short s = 2345;
        System.out.println(s);

        float f = 123.45f;//4 bytes
        double d = 123.45;

        char ch = 'a';

        boolean bool = true;
        System.out.println(bool);
    }
}
