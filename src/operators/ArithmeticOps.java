package operators;

public class ArithmeticOps {
    public static void main(String[] args) {
        int a =10;
        int b = 2;

        int c= a/b;//10/2= 5
        System.out.println(c);//5

        int d = a%b; //return remainder 10/2 =0
        System.out.println(d);//0

    }
}
