package operators.ControlStatements.LoopingStatements;
/*
10 9 8 7
 */
public class ReverseCounting {
    public static void main(String[] args) {
        int n=10;
        for(int i=n;i>=1;--i){
            System.out.println(i);
        }

        for(int i=n;i>=1;--i){
            System.out.println(i);
        }
    }
}
